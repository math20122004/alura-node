let num1 = 55
let num2 = 5

console.log('Subtração: ', num1 - num2)
console.log('Adição: ', num1 + num2)
console.log('Multiplicação: ', num1 * num2)
console.log('Divisão: ', num1 / num2)
console.log('Elevado: ', num1 ** num2)
console.log('Regra Matemática: ', num1 + num2 * num1)
console.log('Regra Matemática 2: ', (num1 + num2) * num1)
console.log('Resto Divisão: ', num1 % num2)

// Prefixo adiciona ou substraí na hora
console.log('Incremento Prefixo: ', ++num1);
// Posfixa adiciona só depois
console.log('Incremento Posfixa: ', num1++);
console.log('Incremento Posfixa Depois: ', num1);
console.log('Decremento: ', num1--);

console.log('Incremento a Mesma Variável: ', num1 *= 3);
console.log('Incremento a Mesma Variável: ', num1 += 3);
console.log('Incremento a Mesma Variável: ', num1 -= 3);
console.log('Incremento a Mesma Variável: ', num1 /= 3);
